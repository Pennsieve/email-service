//go:build ignore

// Command gen reads contract/template-variables.json (the email-templates
// manifest of messageId -> context keys) and generates the typed builders for
// both the Go client (client/messages.go) and the Scala client
// (client-scala/.../Messages.scala). Regenerate with:
//
//	go run ./internal/gen
//
// Keeping the builders generated from the manifest guarantees they never drift
// from the templates' actual variables.
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"sort"
	"strings"
)

func main() {
	raw, err := os.ReadFile("contract/template-variables.json")
	if err != nil {
		fail(err)
	}
	var manifest map[string][]string
	if err := json.Unmarshal(raw, &manifest); err != nil {
		fail(err)
	}

	// Deterministic order.
	ids := make([]string, 0, len(manifest))
	for id := range manifest {
		ids = append(ids, id)
	}
	sort.Strings(ids)

	if err := os.WriteFile("client/messages.go", genGo(ids, manifest), 0o644); err != nil {
		fail(err)
	}
	scalaPath := "client-scala/src/main/scala/com/pennsieve/email/client/Messages.scala"
	if err := os.WriteFile(scalaPath, genScala(ids, manifest), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("generated %d builders -> client/messages.go and %s\n", len(ids), scalaPath)
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}

// pascal converts a kebab-case messageId to a PascalCase identifier
// (dataset-proposal-accepted -> DatasetProposalAccepted).
func pascal(kebab string) string {
	var b strings.Builder
	for _, part := range strings.Split(kebab, "-") {
		if part == "" {
			continue
		}
		b.WriteString(strings.ToUpper(part[:1]))
		b.WriteString(part[1:])
	}
	return b.String()
}

// field converts a context key to an exported field name by upper-casing the
// first letter and preserving the rest (datasetName -> DatasetName, AWSRegion
// -> AWSRegion, AppURL -> AppURL).
func field(key string) string {
	if key == "" {
		return key
	}
	return strings.ToUpper(key[:1]) + key[1:]
}

func genGo(ids []string, manifest map[string][]string) []byte {
	var b bytes.Buffer
	b.WriteString(`// Code generated by internal/gen from contract/template-variables.json; DO NOT EDIT.
//
// Typed builders constructing an EmailRequest for a specific messageId. Each
// owns the exact messageId and context keys the template expects. To change a
// builder, change the template (and the manifest) and regenerate:
//
//	go run ./internal/gen
//
// For a template without a typed builder, use Message().

package client

// Message builds a request for an arbitrary messageId with a raw context map.
// Prefer a typed builder when one exists.
func Message(messageId string, to To, context map[string]any) EmailRequest {
	if context == nil {
		context = map[string]any{}
	}
	return EmailRequest{
		MessageId:  messageId,
		Recipients: []Recipient{to},
		Context:    context,
	}
}
`)
	for _, id := range ids {
		name := pascal(id)
		keys := manifest[id]
		// Args struct.
		fmt.Fprintf(&b, "\n// %sArgs are the variables for the %q template.\n", name, id)
		fmt.Fprintf(&b, "type %sArgs struct {\n", name)
		for _, k := range keys {
			fmt.Fprintf(&b, "\t%s string\n", field(k))
		}
		b.WriteString("}\n")
		// Builder func.
		fmt.Fprintf(&b, "\n// %s builds the %q email.\n", name, id)
		fmt.Fprintf(&b, "func %s(to To, args %sArgs) EmailRequest {\n", name, name)
		fmt.Fprintf(&b, "\treturn Message(%q, to, map[string]any{\n", id)
		for _, k := range keys {
			fmt.Fprintf(&b, "\t\t%q: args.%s,\n", k, field(k))
		}
		b.WriteString("\t})\n}\n")
	}

	out, err := format.Source(b.Bytes())
	if err != nil {
		fail(fmt.Errorf("gofmt generated Go: %w", err))
	}
	return out
}

func genScala(ids []string, manifest map[string][]string) []byte {
	var b bytes.Buffer
	b.WriteString(`// Code generated by internal/gen from contract/template-variables.json; DO NOT EDIT.
//
// Typed builders mirroring the Go client. Regenerate with: go run ./internal/gen
// For a template without a typed builder, use Messages.build(...).

package com.pennsieve.email.client

import io.circe.Json

object Messages {

  /** Untyped escape hatch: build a request for an arbitrary messageId. Named
    * "build" rather than "message" so it never collides with a context key used
    * as a builder parameter (e.g. dataset-revision-needed has a "message" var).
    */
  def build(messageId: String, to: Recipient, context: Map[String, Json] = Map.empty): EmailRequest =
    EmailRequest(messageId = messageId, recipients = List(to), context = context)

  private def str(kvs: (String, String)*): Map[String, Json] =
    kvs.map { case (k, v) => k -> Json.fromString(v) }.toMap
`)
	for _, id := range ids {
		name := lowerFirst(pascal(id))
		keys := manifest[id]
		fmt.Fprintf(&b, "\n  /** Builds the %q email. */\n", id)
		fmt.Fprintf(&b, "  def %s(\n      to: Recipient", name)
		for _, k := range keys {
			fmt.Fprintf(&b, ",\n      %s: String", scalaParam(k))
		}
		b.WriteString("\n  ): EmailRequest =\n")
		fmt.Fprintf(&b, "    build(\n      %q,\n      to,\n      str(", id)
		for i, k := range keys {
			if i > 0 {
				b.WriteString(",")
			}
			fmt.Fprintf(&b, "\n        %q -> %s", k, scalaParam(k))
		}
		b.WriteString("\n      )\n    )\n")
	}
	b.WriteString("}\n")
	return b.Bytes()
}

func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

// scalaParam makes a safe Scala parameter name from a context key. Keys are
// already valid identifiers; we lower-case the first letter for camelCase
// params but keep all-caps prefixes readable (AWSRegion -> awsRegion would be
// odd, so we keep the key verbatim as the param name — it is a valid Scala id).
func scalaParam(key string) string {
	return key
}
